     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(CommentRequest $request)
    {
        //
        $input = $request->all();
  
        Comment::create([
            'post_id' => $input['post_id'],
            'body' => $input['body'],
            'email' => Auth::user()->email,
            'author' => Auth::user()->name,
            'is_active' => 1
        ]);

        return redirect()->back();
    }

